#!/usr/bin/env node

import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'

const CLOUD_WORKFLOWS = new Set(['nano-banana-2', 'kling-o3-i2v'])
const WORKFLOW_TIERS = Object.freeze({
  'z-image-turbo': 'lite',
  'music-gen': 'lite',
  'image-edit': 'standard',
  'multi-angles': 'standard',
  'multi-angles-scene': 'standard',
  'wan22-i2v': 'pro',
  'nano-banana-2': 'cloud',
  'kling-o3-i2v': 'cloud',
})

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(scriptDir, '..')
const outputDir = path.join(repoRoot, 'docs', 'workflow-starter-pack')
const workflowDocsDir = path.join(outputDir, 'workflows')

function uniqueSorted(list = []) {
  return Array.from(new Set((Array.isArray(list) ? list : []).filter(Boolean))).sort((a, b) => a.localeCompare(b))
}

function normalizeWorkflowId(value) {
  const id = String(value || '').trim()
  if (id === 'nano-banana-pro') return 'nano-banana-2'
  return id
}

function getWorkflowTier(workflowId) {
  const normalized = normalizeWorkflowId(workflowId)
  return WORKFLOW_TIERS[normalized] || 'unknown'
}

function getWorkflowRuntime(workflowId) {
  return CLOUD_WORKFLOWS.has(normalizeWorkflowId(workflowId)) ? 'cloud' : 'local'
}

function buildManifestEntry(workflow, workflowPaths, getWorkflowDependencyPack) {
  const workflowId = normalizeWorkflowId(workflow.id)
  const dependencyPack = getWorkflowDependencyPack(workflowId)
  const requiredNodes = uniqueSorted((dependencyPack?.requiredNodes || []).map((node) => String(node?.classType || '').trim()))
  const requiredModels = (dependencyPack?.requiredModels || [])
    .map((model) => ({
      filename: String(model?.filename || '').trim(),
      targetSubdir: String(model?.targetSubdir || '').trim(),
      classType: String(model?.classType || '').trim(),
      inputKey: String(model?.inputKey || '').trim(),
    }))
    .filter((model) => model.filename)
    .sort((a, b) => a.filename.localeCompare(b.filename))

  return {
    id: workflow.id,
    label: workflow.label,
    category: workflow.category,
    description: workflow.description || '',
    tier: getWorkflowTier(workflow.id),
    runtime: getWorkflowRuntime(workflow.id),
    appWorkflowPath: workflowPaths[workflow.id] || null,
    setupWorkflowFile: `workflows/${workflow.id}.comfyui.json`,
    setupWorkflowStatus: 'pending',
    requiresComfyOrgApiKey: Boolean(dependencyPack?.requiresComfyOrgApiKey),
    requiredNodes,
    requiredModels,
  }
}

function renderWorkflowMarkdown(entry) {
  const lines = []
  lines.push(`# ${entry.label}`)
  lines.push('')
  lines.push(`- **Workflow ID:** \`${entry.id}\``)
  lines.push(`- **Category:** \`${entry.category}\``)
  lines.push(`- **Tier:** \`${entry.tier}\``)
  lines.push(`- **Runtime:** \`${entry.runtime}\``)
  lines.push(`- **App Workflow JSON:** \`${entry.appWorkflowPath || 'unknown'}\``)
  lines.push(`- **Setup Workflow File (for ComfyUI users):** \`${entry.setupWorkflowFile}\` (${entry.setupWorkflowStatus})`)
  lines.push('')

  lines.push('## Required Custom Nodes')
  if (entry.requiredNodes.length === 0) {
    lines.push('- None declared')
  } else {
    for (const classType of entry.requiredNodes) {
      lines.push(`- \`${classType}\``)
    }
  }
  lines.push('')

  lines.push('## Required Models')
  if (entry.requiredModels.length === 0) {
    lines.push('- None declared')
  } else {
    lines.push('| Filename | ComfyUI Folder | Loader | Input Key |')
    lines.push('|---|---|---|---|')
    for (const model of entry.requiredModels) {
      const folder = model.targetSubdir ? `models/${model.targetSubdir}` : 'unknown'
      lines.push(`| \`${model.filename}\` | \`${folder}\` | \`${model.classType || '-'}\` | \`${model.inputKey || '-'}\` |`)
    }
  }
  lines.push('')

  lines.push('## API Key')
  lines.push(entry.requiresComfyOrgApiKey
    ? '- Requires Comfy Partner API key in app Settings.'
    : '- Not required.')
  lines.push('')

  lines.push('## Setup Steps')
  lines.push('1. Import this workflow in ComfyUI (using setup workflow file when available).')
  lines.push('2. Install missing custom nodes in ComfyUI Manager.')
  lines.push('3. Download missing models to the expected folders.')
  lines.push('4. Return to ComfyStudio Generate and click re-check.')
  lines.push('')
  return `${lines.join('\n')}\n`
}

function renderIndexMarkdown(entries) {
  const lines = []
  lines.push('# Workflow Starter Pack Index')
  lines.push('')
  lines.push('_Auto-generated by `npm run starter-pack:build`._')
  lines.push('')
  lines.push('| Workflow | ID | Tier | Runtime | Setup Workflow File | Status |')
  lines.push('|---|---|---|---|---|---|')
  for (const entry of entries) {
    lines.push(`| ${entry.label} | \`${entry.id}\` | \`${entry.tier}\` | \`${entry.runtime}\` | \`${entry.setupWorkflowFile}\` | \`${entry.setupWorkflowStatus}\` |`)
  }
  lines.push('')
  lines.push('## Maintenance')
  lines.push('- Add/update workflows in `src/config/workflowRegistry.js`.')
  lines.push('- Keep dependency definitions in `src/config/workflowDependencyPacks.js`.')
  lines.push('- Run `npm run starter-pack:build` after workflow changes.')
  lines.push('- Add actual ComfyUI-importable setup workflow files under this folder before publishing a starter pack release.')
  lines.push('')
  return `${lines.join('\n')}\n`
}

async function loadConfigModules() {
  const registryPath = pathToFileURL(path.join(repoRoot, 'src', 'config', 'workflowRegistry.js')).href
  const dependencyPath = pathToFileURL(path.join(repoRoot, 'src', 'config', 'workflowDependencyPacks.js')).href

  const registry = await import(registryPath)
  const dependency = await import(dependencyPath)
  return { registry, dependency }
}

async function main() {
  const { registry, dependency } = await loadConfigModules()
  const { BUILTIN_WORKFLOWS = [], BUILTIN_WORKFLOW_PATHS = {} } = registry
  const { getWorkflowDependencyPack } = dependency

  const entries = BUILTIN_WORKFLOWS.map((workflow) => (
    buildManifestEntry(workflow, BUILTIN_WORKFLOW_PATHS, getWorkflowDependencyPack)
  ))

  await fs.mkdir(outputDir, { recursive: true })
  await fs.mkdir(workflowDocsDir, { recursive: true })

  const manifest = {
    generatedAt: new Date().toISOString(),
    totalWorkflows: entries.length,
    entries,
  }
  await fs.writeFile(
    path.join(outputDir, 'starter-pack.manifest.json'),
    `${JSON.stringify(manifest, null, 2)}\n`,
    'utf8'
  )

  await fs.writeFile(
    path.join(outputDir, 'INDEX.md'),
    renderIndexMarkdown(entries),
    'utf8'
  )

  for (const entry of entries) {
    await fs.writeFile(
      path.join(workflowDocsDir, `${entry.id}.md`),
      renderWorkflowMarkdown(entry),
      'utf8'
    )
  }

  console.log(`Generated starter pack docs for ${entries.length} workflows.`)
}

main().catch((error) => {
  console.error('Failed to build workflow starter pack:', error)
  process.exitCode = 1
})
